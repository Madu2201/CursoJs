let numero = [1, 2, 3, 4, 5];
numero.push(6); // Adiciona o valor 6 ao final do array
console.log(`O cumprimento do array é: ${numero.length}`); // Exibe o comprimento do array
console.log(`Array em ordem crescente: ${numero.sort()}`) // Ordena o array em ordem crescente

console.log(`Array em ordem decrescente: ${numero.reverse()}`) // Ordena o array em ordem decrescente

console.log(`O valor 3 está no índice: ${numero.indexOf(3)}`); // Exibe o índice do valor 3
